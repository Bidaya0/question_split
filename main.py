import re
import json

def save_to_json(data, filename='questions.json'):
    """将处理后的数据保存为JSON文件"""
    try:
        with open(filename, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        print(f"成功保存 {len(data)} 道题目到 {filename}")
    except Exception as e:
        print(f"保存文件时出错：{str(e)}")



def split_questions(text):
    # 分割成独立的题目块
    blocks = text.strip().split('\n\n')
    questions = []

    for block in blocks:
        lines = [line.strip() for line in block.split('\n')]
        question_part = []
        answer = None
        explanation = None

        # 逆序查找答案行
        for i in reversed(range(len(lines))):
            line = lines[i]
            # 匹配答案行（以A-D开头，可能后面直接跟解释）
            match = re.match(r'^([A-D])(?:\s*(详见.*)?)?$', line)
            if match:
                answer = match.group(1)
                explanation = match.group(2).strip() if match.group(2) else None
                # 处理可能分两行的解释
                if not explanation and i < len(lines)-1 and lines[i+1].startswith('详见'):
                    explanation = lines[i+1]
                    question_part = lines[:i]
                    break
                question_part = lines[:i]
                break

        # 分割题干和选项
        stem_lines = []
        options = []
        option_pattern = re.compile(r'^([A-D])[：:](.*)')
        found_options = False

        for line in question_part:
            # 匹配选项行
            match = option_pattern.match(line)
            if match:
                found_options = True
                options.append({
                    'option': match.group(1),
                    'content': match.group(2).strip()
                })
            else:
                if not found_options:
                    stem_lines.append(line)
                else:
                    # 非选项内容追加到最后一个选项
                    if options:
                        options[-1]['content'] += '\n' + line

        questions.append({
            'stem': '\n'.join(stem_lines).strip(),
            'options': options,
            'answer': answer or '',
            'explanation': explanation or ''
        })

    return questions


